package pack;

public class DataTypesProgram {

	public static void main(String[] args) {
		byte bb=2;
		short s=10;
		int a=100;
		long l=1000;
		char letter='a';
		float f1=10.2f;
		double d=10.236;
		boolean b=true;
		System.out.println("byte value is: "+bb+"\n"+"short value is: "+s);
		System.out.println("integer value is: "+a+"\n"+"long value is: "+l);
		System.out.println("character value is: "+letter+"\n"+"float value is: "+f1);
		System.out.println("double value is: "+d+"\n"+"boolean value is: "+b);

	}

}
